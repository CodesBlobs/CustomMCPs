#!/usr/bin/env node

/**
 * Installs the Chrome native messaging host manifest for Agentic Browser MCP.
 *
 * Usage:
 *   node scripts/install-native-host.mjs --extension-id=<CHROME_EXTENSION_ID>
 *
 * What it does:
 *   1. Creates a shell wrapper (native-host/run.sh) that Chrome will invoke.
 *   2. Generates the final native messaging host manifest JSON.
 *   3. Copies the manifest to the Chrome-expected directory for the current OS.
 */

import { chmod, copyFile, mkdir, writeFile } from "node:fs/promises";
import { homedir, platform } from "node:os";
import path from "node:path";
import { execSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const NATIVE_HOST_NAME = "com.agentic_browser_mcp.host";

const currentDirectory = path.dirname(fileURLToPath(import.meta.url));
const serverPackageDirectory = path.resolve(currentDirectory, "..");
const nativeHostDirectory = path.join(serverPackageDirectory, "native-host");
const nativeHostEntryPath = path.join(serverPackageDirectory, "dist", "nativeHost.js");
const wrapperPath = path.join(nativeHostDirectory, "run.sh");

function parseArgs() {
  const args = process.argv.slice(2);
  let extensionId;
  let uninstall = false;

  for (const arg of args) {
    if (arg.startsWith("--extension-id=")) {
      extensionId = arg.slice("--extension-id=".length).trim();
    }
    if (arg === "--uninstall") {
      uninstall = true;
    }
  }

  return { extensionId, uninstall };
}

function getNativeMessagingHostsDirectory() {
  const os = platform();

  switch (os) {
    case "darwin":
      return path.join(
        homedir(),
        "Library",
        "Application Support",
        "Google",
        "Chrome",
        "NativeMessagingHosts",
      );
    case "linux":
      return path.join(homedir(), ".config", "google-chrome", "NativeMessagingHosts");
    default:
      console.error(
        `Unsupported platform: ${os}. On Windows, you must register the native host manually via the Windows Registry.`,
      );
      process.exit(1);
  }
}

function resolveNodePath() {
  try {
    return execSync("which node", { encoding: "utf8" }).trim();
  } catch {
    return process.execPath;
  }
}

async function install(extensionId) {
  if (!extensionId) {
    console.error(
      "Error: --extension-id=<ID> is required.\n\n" +
        "To find your extension ID:\n" +
        "  1. Load the unpacked extension in chrome://extensions\n" +
        "  2. Copy the ID shown under the extension name\n\n" +
        "Usage:\n" +
        "  node scripts/install-native-host.mjs --extension-id=abcdefghijklmnopqrstuvwxyz123456",
    );
    process.exit(1);
  }

  const nodePath = resolveNodePath();
  console.log(`Node.js path: ${nodePath}`);
  console.log(`Extension ID: ${extensionId}`);

  // Step 1: Create the shell wrapper
  const wrapperContent = [
    "#!/bin/bash",
    `# Auto-generated by install-native-host.mjs — do not edit manually.`,
    `exec "${nodePath}" --enable-source-maps "${nativeHostEntryPath}" "$@"`,
    "",
  ].join("\n");

  await mkdir(nativeHostDirectory, { recursive: true });
  await writeFile(wrapperPath, wrapperContent, { encoding: "utf8", mode: 0o755 });
  await chmod(wrapperPath, 0o755);
  console.log(`Created wrapper: ${wrapperPath}`);

  // Step 2: Generate the manifest JSON
  const manifest = {
    name: NATIVE_HOST_NAME,
    description: "Agentic Browser MCP native host — launches the local bridge and proxies HTTP requests.",
    path: wrapperPath,
    type: "stdio",
    allowed_origins: [`chrome-extension://${extensionId}/`],
  };

  const manifestFileName = `${NATIVE_HOST_NAME}.json`;
  const localManifestPath = path.join(nativeHostDirectory, manifestFileName);
  await writeFile(localManifestPath, JSON.stringify(manifest, null, 2) + "\n", "utf8");
  console.log(`Generated manifest: ${localManifestPath}`);

  // Step 3: Copy the manifest to the Chrome directory
  const chromeNativeHostsDir = getNativeMessagingHostsDirectory();
  await mkdir(chromeNativeHostsDir, { recursive: true });

  const chromeManifestPath = path.join(chromeNativeHostsDir, manifestFileName);
  await copyFile(localManifestPath, chromeManifestPath);
  console.log(`Installed manifest: ${chromeManifestPath}`);

  console.log("\n✅ Native host installed successfully!");
  console.log("   Restart Chrome or reload the extension to activate.\n");
  console.log("Manifest contents:");
  console.log(JSON.stringify(manifest, null, 2));
}

async function uninstall() {
  const { rm } = await import("node:fs/promises");
  const chromeNativeHostsDir = getNativeMessagingHostsDirectory();
  const manifestFileName = `${NATIVE_HOST_NAME}.json`;
  const chromeManifestPath = path.join(chromeNativeHostsDir, manifestFileName);

  try {
    await rm(chromeManifestPath, { force: true });
    console.log(`Removed: ${chromeManifestPath}`);
  } catch {
    console.log(`Not found: ${chromeManifestPath}`);
  }

  try {
    await rm(wrapperPath, { force: true });
    console.log(`Removed: ${wrapperPath}`);
  } catch {
    console.log(`Not found: ${wrapperPath}`);
  }

  const localManifestPath = path.join(nativeHostDirectory, manifestFileName);
  try {
    await rm(localManifestPath, { force: true });
    console.log(`Removed: ${localManifestPath}`);
  } catch {
    console.log(`Not found: ${localManifestPath}`);
  }

  console.log("\n✅ Native host uninstalled.");
}

const { extensionId, uninstall: doUninstall } = parseArgs();

if (doUninstall) {
  await uninstall();
} else {
  await install(extensionId);
}
